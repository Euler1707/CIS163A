public class First // This is the first class, how exciting
{
   public static void main(String[] args) //Method Header
   {
      nameAndAddress(); // the nameAndAdress called and told to run!
      System.out.println("First Java application");
      // Method body  
   }
      
   // Method Header
   public static void nameAndAddress() // This is a method
   {
      System.out.println("Event Handlers Incorporated");
      System.out.println("8900 U.S. Hwy 14");
      System.out.println("Crystal Lake, IL 60014");
      // Method body
      
   }
}   
// Method header is the first line of a method
// contains:
// A return type
// An identifier
// Parentheses

// Quick notes: 
// Main() must be public
// Static means that these methods do not need 
//    an object to be created 
// Public means that it can be accessed by other classes.   
