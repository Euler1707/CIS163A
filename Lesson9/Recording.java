public class Recording
{
   String title;
   String artist;
   int playTime;
   
   public void setTitle(String titl)
   {
      title = titl;
   }
   public String getTitle()
   {
      return title;
   } 
   public void setArtist(String art)
   {
      artist = art;
   }
   public String getArtist()
   {
      return artist;
   }
   public void setPlayTime(int time)
   {
      playTime = time;
   }
   public int getPlayTime()
   {
      return playTime;
   }        
}   